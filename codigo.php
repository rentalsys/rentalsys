<h1>Conservação</h1>

<ul>
	<li>Evite locais sujeitos a vibrações;</li>
	<li>Instale o aparelho sobre uma superfície firme e horizontal, em local com perfeita ventilação;</li>
	<li>Em caso de armário embutido, certifique-se de que não haja impedimento à livre circulação de ar na parte traseira e inferior do aparelho;</li>
	<li>Não apoie sobre tapetes, almofadas ou outras superfícies fofas que obstruam a ventilação;</li>
	<li>Evite locais úmidos, quentes e com poeira;</li>
	<li>Posicione o cabo de rede de modo que fique livre, fora de locais onde possa ser pisoteado, e não coloque qualquer móvel sobre ele;</li>
	<li>Não use substâncias voláteis (benzina, álcool, thiner e solventes em geral) para limpar o gabinete, pois elas podem danificar o acabamento. Use apenas um pano macio, seco e limpo;</li>
	<li>O aplicador deve ser limpo após cada sessão de tratamento, removendo assim todo o acumulo de gel, pois senão a manopla se degrada rapidamente, recomenda-se o uso de álcool com um pano seco;</li>
	<li>Não mergulhar o aplicador em soluções líquidas;</li>
	<li>Limpeza e desinfecção sempre devem ser feitas com o plugue do cabo de alimentação elétrica desligado da tomada de rede.</li>
</ul>

