<h1>Informações Adicionais</h1>

<p><strong>Características dos modos de aplicação:</strong></p>

<ul>
	<li><strong>Varredura:</strong> 1 a 12 J/cm², indicado para áreas com alta densidade e espessura de pelos (emissão pulsada em toda área da demarcação);</li>
	<li><strong>Disparos sequenciais:</strong> 1 a 80 J/cm², emite vários disparos no mesmo ponto, acúmulo de energia (emissão pulsada pontual);</li>
	<li><strong>Disparos pontuais:</strong> 1 a 100 J/cm² no modo pontual, aquecimento pontual e entrega direta (é um único pulso, considerado contínuo).</li>
</ul>

<p><br />
<strong>Características dos comprimentos de onda:</strong></p>

<ul>
	<li><strong>Laser Alexandrite:</strong> comprimento de onda 755nm, indicações para fototipo baixos, pelos finos e mais claros (não brancos), tem atuação superficial, alta afinidade pela melanina e pulsos curtos;</li>
	<li><strong>Laser Diodo:</strong> comprimento de onda 800 a 810nm, indicações para fototipo baixos e altos, pelos finos e grossos. Atuação com profundidade moderada, redução da densidade e espessura de pelo, comprimento de onda mais longo, concentra a energia no bulbo do folículos piloso, menor desconforto e menor chance de efeitos adversos em fototipos mais altos;</li>
	<li><strong>Laser ND: YAG:</strong> comprimento de onda 1064nm, indicações para fototipo alto, pelos finos e grossos. Atuação profunda devido a reduzida absorção pela melanina, se faz mais seguro a aplicação em peles negras, com comprimento de onda mais longo e menor dano dérmico.</li>
</ul>

<p><br />
<span style="color:#7800ff;"><strong>A união desses três comprimentos de onda, bem definido com o tipo de pele, pelo e área tratada, se torna eficiente para a remoção duradoura dos pelos, desde os mais finos até os mais grossos.</strong></span></p>

