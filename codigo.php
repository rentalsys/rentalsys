<h1>Efeitos fisiológicos</h1>

<p>A técnica se baseia na destruição térmica seletiva do alvo constituído pelas células germinativas do folículo piloso que se divide nas fases: anágena, catágena e telógena. O ciclo do pelo, responde a um ciclo fisiológico de crescimento, atuação principal na fase anágena, matriz pigmentada e papila dérmica conectada ao folículo. A fase anágena é a fase de formação do crescimento do pelo baseado em células germinativas, tendo uma grande atividade mitótica, que produz a bainha radicular interna e a haste. É nesta fase que ocorrerá concentração de melanina no bulbo piloso, que apresenta alta função na absorção do laser, resultando em destruição do bulbo piloso. </p>

<ul>
	<li><strong>Fototermólise seletiva:</strong> lesão térmica localizada, provocada por pulsos de radiação absorvida seletivamente pelos cromóforos específicos;</li>
	<li><strong>Ação fotoquímica:</strong> após a absorção da luz emitida, o cromóforo sofre uma reação fotoquímica e dissipa a energia em forma de calor.</li>
</ul>

